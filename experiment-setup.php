            <h3>What are we investigating?</h3>
            <p>The central idea for Palaeoplasticene is to engage with the long-term life of plastic. In this experiment we speculate into prehistoric times, where an evolutionary tipping point prompted a fungus to grow itself out of plastic. In our case, we have chosen PLA as the plastic of choice, because it is made of lactic acid monomer units, which occurs abundantly in nature. Our speculative past includes a set of environmental conditions that creates evolutionary circumstances where the prehistoric fungus benefits from growing this plastic material.</p>
            <p>The central question of this experiment is: &quot;how does the plastic mushroom change over time&quot;?</p>
            <p>As such, we will run an experiment to see how robust the PLA mushroom is, and to understand what evolutionary advantage there could be to the mushroom of a plastic materiality.</p>
