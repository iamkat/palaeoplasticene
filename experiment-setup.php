        <h3>What is taphonomy?</h3>
            <p>Taphonomy in a broad sense is the study of how organic remains pass from the biosphere to the lithosphere. For our purpose, it&#39;s the study of how matter decays and transforms. In some ways it can be understood as the study of entropy. It is commonly integrated with palaeontological research to better understand and thereby reconstruct the process of decay that results in what is observed in the fossil record. Taphonomic processes act over very long times, thus it is notoriously difficult to study experimentally. In regular taphonomic experiments, researchers would account naturally decaying carcasses over many years, or, in small scale studies they would leave a carcass in the open and return regularly to record its state of decomposition, including the action of other creatures in hastening the breakdown of the body. In the end, they can observe how many bones remain over time, how the bones are distributed and how they have been transformed, thereby giving a hint to the circumstances that may have surrounded the death and decomposition of a body from prehistory. Understanding the process of decay and transformation is equally relevant for forensic investigations, including those of the very recent past as well.</p>
