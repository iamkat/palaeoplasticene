        <h3>How to document the experiment</h3>
        <p>The aim of the documentation is to show the change over time of the prehistoric mushrooms. The best way to do that is to ensure you document using the same frame for the photo each time. As it can be impractical to have a camera in place outdoors long term, it&#39;s important to maximise your possibilities for taking photos from the same distance and angle time after time.</p>
        <ul>
            <li>Take photos at the same time of day across the experiment.</li>
            <li>Take photos from:
                <ul>
                    <li>front and each side of the pot at an angle of around 45 degrees from the horizontal surface of the soil inside</li>
                    <li>top view looking directly downwards</li>
                </ul>
            </li>
            <li>Aim to fill 25% of the frame with the prehistoric mushrooms. If you are unable to also include the whole of the pot, you can take an additional view from further away.</li>
            <li>Make sure to capture the different angles in approximately the same position for every shot:
                <ul>
                    <li>Take photos from the same distance. Use either measuring tape or a knotted length of string to gauge distance.</li>
                    <li>Take photos from the same angle: This is most easily achieved with a tripod, where you set the height and angle according to recorded values. If you don&#39;t have a tripod, find a chair, block, brick or similar on which you can rest your elbows to achieve approximately the same height each time.</li>
                </ul>
            </li>
            <li>A good way to check is to compare your current camera preview with the last photograph of that angle you took: if the size of the object in the previous photo is the same as in your current shot then the distance to the object is pretty much the same (assuming that you do not change the zoom or focal length of your camera)</li>
            <li>If you are using a tripod and have the option to, it can be helpful to mark the position of the tripod&#39;s feed with masking tape. Make a note of the orientation of the camera with respect to the tripod and any angles on the head of the tripod.</li>
            <li>Be careful not to get your own shadow in the photo frame.</li>
            <li>Make sure to focus the camera on the same part of the prehistoric mushroom each time.</li>
            <li>If your camera allows you to control it, use a wide depth of field - ie: set your F-stop to a higher number (small lens aperture), which shows detail over a larger distance from the camera and allows you to capture detail from the whole mushroom.</li>
        </ul>
